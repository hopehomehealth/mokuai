/************************************************************************/
/* 利用递归执行顺序是从最底层开始，实现任意长度字符串的逆序                                                                     */
/************************************************************************/

#include<iostream>
using namespace std;

void f(char *p)
{
	if ('\0'!=*p)
	{
		f(p + 1);
		cout <<"输出值： "<< *p<<endl;
	}
}


void main()
{

	f("123456");
}